
public class second {
	public  double sum(double u, double a){
		double z = u;
		double x = a;
		double sum = z+x;
		return sum;
	}
	
	public  double min(double u, double a){
		double z = u;
		double x = a;
		double min = z-x;
		return min;
	}
	
	public  double div(double u, double a){
		double z = u;
		double x = a;
		double div = z/x;
		return div;
	}
	
	public  double mul(double u, double a){
		double z = u;
		double x = a;
		double mul = z*x;
		return mul;
	}

}
